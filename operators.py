#Arithmetic operators

num1 = 34
num2 = 10

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) #modulus - returns the remainder after dividing two numbers

#comparison operators
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2) #equal to
print(num1 != num2) #not equal to

#assignment operators
w = 7
print(w)

w += 3
print(w)

b = 10
b -= 2
print(b)

#logic operators -and,or,not
print(56 < 100 and 45 < 23)
print(56 < 100 or 45 > 23)
print(not(56 < 100 or 45 > 23))

